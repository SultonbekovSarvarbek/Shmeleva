              страны, но и над государственным аппаратом. Основные направления
              пролонгированной криминальной деятельности ОПГ это: стремление
              монополизации в определенных сферах экономики и территории;
              проникновения во властные структуры; выход за национальные границы
              и присоединение к транснациональной преступности. В результате
              можно выявить конечную цель: политизация преступности и
              криминализация власти. «Криминальный бизнес возникает, существует
              и развивается при наличии ряда условий: спрос на нелегальные
              товары (наркотики, оружие и др.) и услуги (сексуальные и др.);
              неудовлетворенный спрос на легальные товары и услуги (например,
              «дефицит», присущий социалистической экономике); рынок труда,
              безработица, незанятость подростков и молодежи; пороки налоговой,
              таможенной, финансовой политики государства, препятствующие
              нормальному развитию легальной рыночной экономики». Таким образом,
              из вышесказанного можно отметить ряд признаков организованной
              преступности. Первым признаком выступает наличие объединения лиц,
              имеющих выраженную иерархическую структуру, со строгой
              дисциплиной, устойчивой системой уголовных традиций для реализации
              преступной деятельности. Вторым признаком, характеризующим
              организованную преступность является экономический. Стремление
              оргпреступности придать внешнюю легальность своей противозаконной
              деятельности является неотъемлемой чертой организованной
              преступности, выделяющую данную форму криминальной деятельности от
              всех иных форм преступности. Этой цели служит и отмывание доходов
              от преступной деятельности, что не присуще тем видам криминальной
              активности, в пределах которых их участники ограничиваются
              распоряжением имуществом и различными ценностями, которыми
              незаконно завладели (имущественные преступления,
              корыстно-насильственные преступления, и пр.). Последним и самым
              важным признаком, который усложняет раскрытие преступлений, путем
              обеспечения безопасности субъектов организованной преступности,
              является коррупция. На основании вышеизложенного можно подчеркнуть
              уровень развития организованной преступности. Своими
              противоправными действиями этот институт фактически овладел
              основными рычагами по управлению обществом, внедряя свои интересы,
              спокойно осуществляет главную цель-извлечение максимум прибыли.
              Все это свидетельствует о многообразии видов организованной
              преступной деятельности и о достаточно высоком уровне успешного
              внедрения в нужные им структуры. Международная классификация видов
              деятельности организованной преступности. Международное уголовное
              право и криминологическое учение, выделяют следующие виды
              криминального «бизнеса» ОПГ: 1. Торговля людьми, которая с
              криминологической точки зрения, может быть подразделена на
              следующие формы: • похищение и купля-продажа человека; • торговля
              людьми, для дальнейшего использования в сексуальном рабстве; •
              торговля людьми для использования их органов и тканей. 2.
              Наркобизнес. «Наркобизнес» - это бизнес, который приносит доход от
              незаконной торговли наркотиками. Торговля наркотиками приносит
              большую прибыль и имеет наибольшую степень специализаций. Этот
              своеобразный бизнес интересует организованные группы тем, что
              можно образовать определенную сеть постоянно поступающей прибыли.
              Опасность проявления деятельности ОПГ в сфере наркобизнеса
              заключается в том, что при перевозке наркотических веществ
              используются множество государственных звеньев, таких как
              таможенные службы, правоохранительные органы, государственные
              деятели и чиновники. Это в первую очередь говорит о
              коррумпированности населения. Люди, в чьи обязанности входит
              защита населения и поддержание его здоровья, а так же
              беспрепятственного осуществления своих прав и многое другое,
              зачатую не соблюдают свои обязанности, что приводит к медленному
              уничтожению нации 3.Торговля оружием. За незаконными поставками
              оружия стоит международная оргпреступность. Незаконный оборот
              оружия существенно влияет на криминогенную ситуацию в мире.
              Организованная преступность имеет огромный интерес в сфере
              торговли оружием. Во-первых, это прибыль с незаконного бизнеса,
              во-вторых, возможность контролировать политическую и экономическую
              ситуации в странах. Это порождает целый ряд тяжких и особо тяжких
              преступлений, создавая почву для процветания терроризма и угрозу
              войны в мировых масштабах. 4. Экстремизм. С точки зрения
              криминологии, экстремизм – это есть наивысшая точка общественной
              опасности по отношению к мировому сообществу. И, он является
              собирательным понятием, которое может проявится в любых формах
              Экстремизм не может иметь религиозную или иную
              социально-нравственную тенденцию, так как он, по своей сути,
              преступная агрессивная деятельность наиболее общественно-опасного
              преступного элемента. Основная цель экстремизма проста,- это
              получение власти и контроля над регионом, обществом или
              государством. Необходимо отметить, что вышеперечисленные
              преступления не носят единичный характер. В первую очередь, это
              говорит о том, что деятельность ОПГ направлена на постоянное
              получения прибыли. Имеется определенный круговорот в криминальной
              среде. Данная категория преступлений не порождает единичного
              случая, как грабеж или разбой, а наоборот, образуют целую систему
              криминальных операций, направленную на извлечение сверхприбыли.
              Криминологические критерии общественной опасности организованной
              преступности. При описании критериев общественной опасности
              необходимо отметить, что исходя из основ уголовного права,
              общественная опасность подразделяется на два критерия:1)
              качественный критерий, определяющий характер общественной
              опасности; 2) количественный критерий, отражающий степень
              общественной опасности. Качественный критерий определяется
              содержанием тех общественных отношений, которые подверглись
              преступному посягательству (объект преступления). Характеристика
              качественного критерия общественной опасности организованной
              преступности подразумевает рассмотрения структуры организованной
              преступности, видов и форм преступных группировок. Количественный
              критерий выражает степень общественной опасности и оказывает
              прямое влияние на назначение наказания за совершенное преступное
              деяние. Степень общественной опасности устанавливается методом
              сравнения преступлений, размером нанесенного ущерба, видом вины и
              т.д. В уголовном законе существуют следующие формы организованных
              преступных формирований: Террористическое сообщество («Организация
              террористического сообщества и участие в нем», ст.205.4 УК РФ);
              Незаконное вооружённое формирование («Организация незаконного
              вооруженного формирования или участие в нем», ст.208 УК РФ); Банда
              («Бандитизм», ст.209 УК РФ); Преступное сообщество («Организация
              преступного сообщества (преступной организации) или участие в нем
              (ней)», ст.210 УК РФ); Экстремистское сообщество («Организация
              экстремистского сообщества», ст.282.1 УК РФ); Объединения,
              посягающие на личность и права граждан («Создание некоммерческой
              организации, посягающей на личность и права граждан», ст.239 УК
              РФ). Финансовая независимость ставится в противовес
              нормально-функционирующему обществу. Существование того или иного
              вида деятельности показывает: • современную тенденцию общества в
              востребованности данных отраслей деградированния; • согласие
              социума на существование в рядах международного рынка запретных
              веществ; • оказания услуг и предоставления контроля над странами;
              • нежелания государства пойти на полную реконструкцию механизмов
              административного функционирования государственного аппарата.
              Глава II. Криминологические аспекты существования организованной
              преступности и международное сотрудничество борьбы с ней.
              Причинный комплекс поддержания и усиления позиций организованной
              преступности в мире. Для противодействия данному преступному
              институту необходимо отметить главные рычаги, обеспечивающие столь
              длительное существование организованной преступности, в том числе
              аспекты укрепления и усиления своих позиций на международном
              уровне. Для начала стоит обратить внимание на этапы становления
              организованной преступности в России. Конец 70-х начало 80-х гг.
              ХХ в. - период становления организованной преступности. Условиями
              возникновения послужили: застой и распад экономики; развитие
              кооперативного и фермерского движения, иными словами образования
              предпринимателей и создание нового вида бизнеса; переоценка
              ценностей, конфликты поколений; наличие запрещенных видов
              деятельности, которые способны приносить прибыль (проституция,
              торговля людьми, наркобизнес). Выше указаны основные параметры,
              ставшие основанием для возникновения криминального бизнеса. Конец
              80-х начало 90-х гг. ХХ в.- укрепления своих преступных позиций в
              бизнесе, нестабильность во властных структурах, разрушение
