              занятие пролонгированной криминальной деятельностью для заработка,
              нравственный упадок в обществе. Конец 90-х гг. середина 2000-х гг.
              - дальнейшее развитие, внедрения в мировые криминальные сети,
              внедрение в легальный бизнес, вхождение во властные структуры.
              Современный этап с середины 2000-х гг.и до сегодняшнего дня.
              Данный этап можно характеризовать как выстраивание со стороны
              локальной ОПГ связей с международным криминологическим бизнесом;
              появление новых форм и видов деятельности организованной
              преступности; усиленное сращивание или налаживание контактов
              (коррупционное составляющее) с системой правоохранительных
              органов, таможенных органов и т.д.; активное стремление к
              воплощению цели: власть над миром, полная монополия своего
              криминального бизнеса. Характеристика этапов становления и
              зарождения организованной преступности дает возможность сделать
              вывод о том, что дестабилизация любых общественных процессов в
              обществе, особенно в экономической сфере, ведет к появлению
              криминальных видов деятельности и распаду общества. В детерминации
              организованной преступности важную роль играют: Пробелы в
              (культурной, политической, социальной, экономической) сферах жизни
              общества. Латентность организованной преступности. Отсутствие
              соответствующей законодательной базы (УК РФ не содержит отдельную
              главу, посвященную преступлением организованной преступности) и
              отсутствие жесткой системы наказаний за совершения преступлений
              организованными преступными группами; Пробелы в политической
              системе общества (чиновники заняты другими вопросами, не видят
              острой глобальной гуманитарной катастрофы). Межгосударственные
              противоречия (конфликты и ,соответственно, отсутствие возможности
              координировать усилия множество стран в борьбе с организованной
              преступностью. Международное сотрудничество в сфере
              противодействия ОПГ. С точки зрения уголовного права РФ,
              организованная преступность понимается как «…деятельность
              устойчивых преступных сообществ (организаций), отличающихся
              иерархическим организационным построением, сплоченностью на
              конкретной преступной платформе (бандитской, антиконституционной,
              контрабандно-валютной. националистической и т.д.) отработанной
              системой конспирации и защиты от правоохранительных органов,
              коррумпированностью, масштабностью преступной деятельности,
              включая выход за рубеж и связь с международной мафией».
              Международное уголовное право содержит понятие организованной
              преступности в «Конвенции против транснациональной организованной
              преступности», где понятие «…организованная преступная группа"
              означает структурно оформленную группу в составе трех или более
              лиц, существующую в течение определенного периода времени и
              действующую согласованно с целью совершения одного или нескольких
              серьезных преступлений или преступлений, признанных таковыми в
              соответствии с настоящей Конвенцией, с тем, чтобы получить, прямо
              или косвенно, финансовую или иную материальную выгоду».
              Противодействие организованной преступности в России зависит не
              только от системы правоохранительных органов. Россия вступила в
              международные полицейские организации, в структуре МВД России
              создано Национальное бюро Интерпола, где происходит
              противодействию коррупции и экстремизму. Основными принципами
              международного сотрудничества в борьбе с транснациональной
              организованной преступностью являются: Систематизированный,
              комплексный подход к проблеме борьбы с организованной
              преступностью; Сужения возможностей для преступной деятельности
              политическими и экономическими методами; Уменьшение уязвимости
              законной экономики; Целенаправленная деятельность по подрыву
              экономической базы организованной преступности; Формирование
              унифицированного законодательства. В целях укрепления своих
              позиций по борьбе с преступностью, Россией была подписана и
              ратифицирована Конвенция Организации Объединенных Наций против
              транснациональной организованной преступности от 15 ноября 2000
              года. Затем был издан закон «О ратификации Конвенции Организации
              Объединенных Наций против транснациональной организованной
              преступности и дополняющих ее Протокола против незаконного ввоза
              мигрантов по суше, морю и воздуху и Протокола о предупреждении и
              пресечении торговли людьми, особенно женщинами и детьми, и
              наказании за нее"». Основной целью для России служило создание
              специально выработанных программ, обеспечивающих единое
              международное сотрудничество для противодействия ОПГ. «Реализация
              указанной и последующих Программ позволила создать необходимую
              нормативно-правовую базу межгосударственного сотрудничества в
              борьбе с различными проявлениями ОП». Необходимо отметить, что
              наравне с государственными организациями большую значимость в
              совместном противодействии организованной преступности имеют
              неправительственные учреждения, такие как: Международная
              ассоциация уголовного права; Международное криминологическое
              общество; Международное общество социальной защиты, Всемирное
              виктимологическое общество. Принципы ООН в сфере противодействия
              организованной преступности не только раскрывают стратегию
              предупреждения преступлений, совершаемых организованной
              преступности, но и определяют основные формы международного
              сотрудничества. В первую очередь принципы ООН предлагают
              согласовать законодательства разных стран, согласно рекомендациям
              Конгрессов ООН по пресечению и предупреждению преступности, а так
              же по взаимодействию с правонарушителями. Естественно, вести обмен
              информацией, связанной с состоянием организованной преступности и
              о всех преступлениях, совершаемых ОПГ, так же не стоит забывать об
              экстрадиции в рамках заключенных договоров и взаимное правовое
              обеспечение. Одним из ключевых звеньев выступает сотрудничество
              правоохранительных органов в рамках Интерпола, Европола, которое
              будет включать обмен информацией, совместная подготовка кадров,
              обмен опытом, консультация, взаимная помощь в аресте и конфискации
              доходов от преступной деятельности. Концепция по противодействию
              организованной преступности. Организованная преступность – это
              сложно - структурированный криминальный институт, деятельность
              которого направлена на извлечение сверхприбыли, а равно
              установление контроля над государственным аппаратом отдельной
              страны и мирового сообщества, путем внедрения коррупционного
              коэффициента в мировые сети политического устройства, что приводит
              к дестабилизации общественно-политического строя и уничтожение
              нации. «Необходимо помнить, если величайшее поощрение
              организованной преступности-это безнаказанность, то лучшее
              лекарство против нее-неотвратимость наказания!». Для
              противодействия организованной преступности все мировое сообщество
              должно направить свои усилия на реализацию в каждом государстве
              следующей концепции, основанной на примере Российской Федерации
              (См. Схема № 5). Исходя из пробелов в государственных системах,
              которыми пользуются ОПГ, осуществляя свою деятельность, можно
              выделить основные сферы работы концепции. Первой выступает
              законодательная, которая включает в себя следующие рекомендации:
              формирование пакета законодательных актов, которые будут
              ратифицировать деятельность правоохранительных органов по борьбе с
              организованной преступностью; внесение кардинальных изменений в
              Проект ФЗ «О борьбе с организованной преступностью», посредством
              четкого определения, что же такое организованная преступность;
              выработать единое определение организованной преступности для
              всего мирового сообщества; Внесение нового раздела в Уголовный
              Кодекс, посвященный преступлениям совершенным организованной
              преступностью; увеличение ответственности за создание
              организованного преступного сообщества и участие в нем. Так как в
              ходе криминологического исследования, было выяснено, что основой
              существования организованной преступности является коррупция, то
              на законодательном уровне необходимо обязательно ввести
              конфискацию имущества, как вида уголовного наказания по
              коррупционным составам и повысить ответственность за
              коррумпирование. Следующим звеном в противодействие организованной
              преступности выступает политическая обстановка. Для ослабления
              данного института необходимо объединить усилия мирового сообщества
              в борьбе с организованной преступностью и устранить
              межгосударственные противоречия. Так как деятельность
              оргпреступность плотно проникла в сознание людей, то государство
              должно вводить обязательность правового воспитания и,
              соответственно, поощрять население. Проблему социального
              неравенства очень трудно устранить, но можно обеспечить более
              высокий уровень жизнь малообеспеченному населению. Как показывает
              практика, преступления совершают люди из неблагополучных семей,
              без образования. Это все свидетельствует, что необходимо подминать
              уровень грамотности и предоставлять реальные возможности бюджета в
              образовательных учреждениях. На сегодняшний день, подобные
              программы существуют. Однако реальность зачисления в институт не
              на коммерческой основе сводится к минимуму из-за повышенных
              требований к абитуриентам. Одной из основных причин разжигания
              агрессии к другим государствам выступает СМИ. Поэтому необходимо
              ограничивать власть данному бесконтрольному институту. Заключение
              В результате проведенного криминологического исследования
              организованная преступность представляет серьезную проблему для
              общества и государства. Организованная преступность - это сложно -
              структурированный криминальный институт, деятельность которого
              направлена на извлечение сверхприбыли, а равно установление
              контроля над государственным аппаратом отдельной страны и мирового
              сообщества, путем внедрения коррупционного коэффициента в мировые
              сети политического устройства, что приводит к дестабилизации
              общественно-политического строя и уничтожение нации. Сложность
              раскрытия и выявления преступлений, совершаемых оргпреступностью,
              заключается в уникальности ее становления: внутригосударственном и
              мировом. Широко масштабность криминального влияния организованной
              преступности на общество обеспечивается ее подразделением.
              Невозможно рассматривать организованную преступность исключительно
              внутри государства отдельной страны, так как изначально зарождение
              данного противоправного института происходит на мировом уровне.
              Пробелы в законодательствах стран, а так же в конвенциях дают
              подходящую почву для деятельности ОПГ. Они пользуются сложившиеся
              обстановкой в мире, тем самым реализуют свой нелегальный бизнес,
              путем обеспечения черного рынка оружие, предоставления различных
              услуг (сексуальных, рабовладельческих), пороки таможенных служб
              предоставляют право на внедрения наркобизнеса в государства. Все
              это усиливается воздействиям и призывам экстремистских сообществ,
              которые, в свою очередь, подрывают общественно-политический строй
              и порядок в каждой стране. Что приводит к разложению государства и
              уничтожению нации. Поскольку по своей природе, организованные
              преступные группировки преследуют цель извлечение максимум
              прибыли, соответственно, основной их направленностью является не
              совершение конкретных преступлений, а проникновение в экономику
              для установления контроля не только над экономической жизнью
              страны, но и над государственным аппаратом. Данный факт,
              свидетельствует о многообразии видов организованной преступной
              деятельности и о достаточно высоком уровне успешного внедрения в
              нужные им структуры. Не опасаясь и не боясь никаких рисков и
              ответственности, организованные сообщества обеспечивают
              безопасность в осуществлении криминальной деятельности, путем
              спонсирования необходимых им структур. Предупреждение
              преступлений, совершаемых ОПГ обуславливается выявлением и
              выделением основного фактора, обеспечивающим базис формирования и
              развития организованной преступности – это коррупция. Коэффициент
              коррупции создает исключительно финансово - экономическую
              составляющую оргпреступности. Коррупция является основой
              существования организованной преступности, и является ее главным
              признаком, так как без нее в современном мире, какой бы ни была
              сплоченность организованной преступной группы, при существовании
              современного оборудования используемого для раскрытия
              преступлений, никакая организованная преступная группа не смогла
              бы просуществовать и месяца. В целях борьбы с организованной
              преступностью необходимо ввести такое наказание, как конфискация
              имущества. Предложенный вид наказания существенно закроет дыры в
              бюджете страны, образованные от преступлений совершаемых ОПГ. Для
              усовершенствования законодательной базы в РФ необходимо ограничить
              распространения Условно-досрочного освобождения от отбывания
              наказания по составам, раскрывающим виды деятельности
              организованной преступности. Таким образом, необходимо дополнить
              п. 5 ст.79 УК РФ «Условно-досрочное освобождение от отбывания
              наказания», внесением п. 5.1. положения, о том, что к лицам
              совершившим преступления, предусмотренные ст. 127.1 УК РФ,
              127.2.УК РФ, ст. 205 - 205.4 УК РФ, ст.208 УК РФ, ст.210 УК РФ,
              228.1 УК РФ, 229.1 УК РФ ,ст.282.1 РФ, и УДО не применять. Помимо
              всего прочего, организованная преступность занимается вовлечением
              малолетних и несовершеннолетних лиц в свой нелегальный бизнес. За
              преступления, предусмотренные п. а) ч.3 ст. 230 УК РФ, п. б) ч.2,
              ст. 127.1, п. б) ч. 2 ст. 127.2 УК РФ, автор допускает возможность
              введения такого вида наказания, как смертная казнь.
