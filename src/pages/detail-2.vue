          <p>
            На сегодняшний день весьма остро стоит проблема существования
            организованной преступности в России, подавляя своим вне властным
            авторитетом все общепринятые, моральные и нравственные устои и
            представления социума о правовом государстве. При выделении
            организованной преступности в отдельный институт противоправного
            поведения, необходимо подчеркнуть степень и характер организованного
            взаимодействия нескольких преступников между собой для реализации
            своей пролонгированной криминальной деятельности. На базе данного
            критерия представлено множество определений этого явления.
            Обобщенная характеристика и понятие организованной преступной
            деятельности были отражены в докладе Генерального секретаря ООН «
            Воздействие организованной преступной деятельности на общество в
            целом». В нем сформулировано понятие организованной противоправной
            деятельности и выделяется перечень признаков, которые указывают на
            характер организованной преступности. Так, в п. а) данного доклада
            под организованной преступностью понимается «деятельность
            объединений преступных лиц или группировок, объединившихся на
            экономической основе. Эти группировки очень напоминают банды периода
            феодализма, которые существовали в средневековой Европе.
            Экономические выгоды извлекаются ими представления незаконных услуг
            и товаров или путем предоставления законных услуг и товаров в
            незаконной форме». Еще имеется одно из понятий, которое, на наш
            взгляд, конкретизирует организованную преступность и более точно
            указывает на признаки данного противоправного института. В
            Интерполе, организованной преступностью называют «деятельность
            преступной группы, имеющей корпоративную структуру, чьей главной
